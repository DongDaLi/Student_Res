package com.dongli.entity;



public class Pay {
    private String IDNum;
    private String name;
    private String money;
    private String payDate;
    private int tuition;
    private int dormitoryFee;
    private int costumes;
    private String result;
    private String proofMaterial;
    private int loan;
    private String green;

    public String getIDNum() {
        return IDNum;
    }

    public void setIDNum(String IDNum) {
        this.IDNum = IDNum;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMoney() {
        return money;
    }

    public void setMoney(String money) {
        this.money = money;
    }

    public String getPayDate() {
        return payDate;
    }

    public void setPayDate(String payDate) {
        this.payDate = payDate;
    }

    public int getTuition() {
        return tuition;
    }

    public void setTuition(int tuition) {
        this.tuition = tuition;
    }

    public int getDormitoryFee() {
        return dormitoryFee;
    }

    public void setDormitoryFee(int dormitoryFee) {
        this.dormitoryFee = dormitoryFee;
    }

    public int getCostumes() {
        return costumes;
    }

    public void setCostumes(int costumes) {
        this.costumes = costumes;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public int getLoan() {
        return loan;
    }

    public void setLoan(int loan) {
        this.loan = loan;
    }

    public String getGreen() {
        return green;
    }

    public void setGreen(String green) {
        this.green = green;
    }

    public String getProofMaterial() {
        return proofMaterial;
    }

    public void setProofMaterial(String proofMaterial) {
        this.proofMaterial = proofMaterial;
    }
}
